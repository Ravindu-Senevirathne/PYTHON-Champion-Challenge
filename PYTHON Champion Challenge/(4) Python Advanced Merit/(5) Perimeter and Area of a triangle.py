# Perimeter
print("Finding the Perimeter...")
P1 = int(input("Enter a value for one side : "))
P2 = int(input("Enter a value for the other side : "))
P3 = int(input("Enter a value for the other side : "))

P = P1 + P2 + P3
print("Perimeter is : ", P, "cm")

# Area
print("Finding the Area...")
Base = int(input("Enter a value for the base : "))
Height = int(input("Enter a value for the height : "))

A = 1/2 * Base * Height
print("Area is : ", int(A), "cm 2")
